public class CalculateValues {

    public int addValues(int a, int b)
    {
        return a+b;
    }

    public int subtractValues(int a, int b)
    {
        return a-b;
    }

    public int multiplyValues(int a, int b)
    {
        return a*b;
    }

    public int divideValues(int a, int b)
    {
        return a/b;
    }
}
